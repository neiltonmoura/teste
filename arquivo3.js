let dataCompleta;
dataCompleta = new Date()
let diaAtual = dataCompleta.getDay();

alert(diaAtual)

switch(diaAtual){
    case 0:
    alert("Domingo")
    break;
    case 1:
    alert("Segunda-feira")
    break;
    case 2:
    alert("Terça-feira")
    break;
    case 3:
    alert("Quarta-feira")
    break;
    case 4:
    alert("Quinta-feira")
    break;
    case 5:
    alert("Sexta-feira")
    break;
    case 6:
    alert("Sábado")
    break;

    
}

